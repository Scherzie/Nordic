public class Switch {
    public static void main (String args []){
        int x = 25;
        switch (x){
            case 20 :
                System.out.println("case ke 20");
                break;
            case 21 :
                System.out.println("Case ke 21");
                break;
                default:
                    System.out.println("Semua Salah");
        }
    }
}
