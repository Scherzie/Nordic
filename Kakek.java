public class Kakek {
    public static void main (String [] args)
    {
        System.out.println(sawahk(1));
        System.out.println(kebunk("a"));
        System.out.println(rumahk(1));
    }
    public static float sawahk (float sw)
    {
        sw = 1000 / 100;
        return sw;
    }
    public static String kebunk (String kb)
    {
        kb = "Nilai awal kebun Kakek : 1000." + " Akan dibagi 10 karena terjerat hutang!";
        return kb;
    }
    public static float rumahk (float rmk)
    {
        rmk = 4;
        return rmk;
    }
}
